//
// 1. РЕДАКТИРОВАНИЕ АБИТУРИЕНТА (Кнопка "Изм.")
//    - При нажатии на кнопку "Изм." открывается мастер добавления (ApplicantWizardPage)
//    - Мастер содержит пошаговую навигацию по всем шагам ввода данных:
//      1. Удостоверение личности
//      2. Контакты
//      3. Вид заявления и образование
//      4. Документы
//      5. Родственники
//      6. Доп. сведения
//      7. Заявление, выбор конкурсов
//      8. Инд. достижения
//      9. Приоритеты
//      10. Приложенные документы
//    - Все поля будут заполнены данными найденного абитуриента
//    - На каждом шаге можно быстро переходить между разделами через меню шагов
//
// 2. УДАЛЕНИЕ АБИТУРИЕНТА (Кнопка "Удал.")
//    - При нажатии на кнопку "Удал." появляется диалог подтверждения
//    - Пользователь должен подтвердить удаление (Yes/No)
//    - При подтверждении:
//      * Абитуриент удаляется из базы данных
//      * Удаляются ВСЕ связанные данные:
//        - Документы удостоверения личности (IdentityDocuments)
//        - Прикрепленные документы (AttachedDocuments)
//        - Языки (ApplicantLanguages)
//        - Спортивные достижения (SportAchievements)
//        - Индивидуальные достижения (IndividualAchievements)
//        - Родственники (Relatives)
//        - Заявления и конкурсы (ApplicationPriorities)
//        - Документы об образовании (Documents)
//        - Контактная информация (ContactInformation)
//      * Абитуриент удаляется из списка результатов поиска
//      * Показывается сообщение об успешном удалении
//
// ============================================================================
// КОД ИЗМЕНЕНИЯ
// ============================================================================
//
// В XAML добавлена кнопка:
//     <Button Content="Удал." FontSize="10" Width="45" Height="22" Margin="2"
//             Background="#F44336" Foreground="White" ToolTip="Удалить"
//             Click="DeleteButton_Click" Tag="{Binding}"/>
//
// В Code-Behind добавлены три метода:
//
// 1. DeleteButton_Click() - обработчик события нажатия кнопки удаления
// 2. OpenApplicant() - обновлен для открытия мастера в режиме редактирования
// 3. DeleteApplicant() - основная логика удаления с подтверждением
//
// ============================================================================
// ИСПОЛЬЗОВАНИЕ
// ============================================================================
//
// Пользовательский сценарий:
//
// 1. На странице "Поиск абитуриентов":
//    - Введите ФИО и нажмите "Поиск"
//    - В таблице результатов выберите абитуриента
//
// 2. Для редактирования:
//    - Нажмите кнопку "Изм." (оранжевая)
//    - Откроется мастер с заполненными данными абитуриента
//    - Измените необходимые данные
//    - Используйте навигацию между шагами через меню шагов или кнопки
//    - На последнем шаге нажмите "Завершить" для сохранения
//
// 3. Для удаления:
//    - Нажмите кнопку "Удал." (красная)
//    - Подтвердите удаление в появившемся диалоге
//    - Абитуриент и все его данные будут удалены
//
